def squre(length, angle):
    
    mitul.forward(length)
    mitul.right(angle)
    mitul.forward(length)
    mitul.right(angle)
   
    mitul.forward(length)
    mitul.right(angle)
    mitul.forward(length)
    mitul.right(angle)

