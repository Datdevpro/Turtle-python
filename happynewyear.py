def draw_star(size, color):
    angle = 120
    mitul.fillcolor(color)
    mitul.begin_fill()

    for side in range(5):
        mitul.forward(size)
        mitul.right(angle)
        mitul.forward(size)
        mitul.right(72 - angle)
    mitul.end_fill()
    return

